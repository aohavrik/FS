# Користувач вводить із клавіатури арифметичний вираз. Наприклад, 23+12.
# Необхідно вивести на екран результат виразу. Для нашого прикладу — це 35.
# Арифметичний вираз може складатися тільки з трьох частин: число, операція,
# число. Можливі операції: +, -, *, /.


exp = input("Введіть арифметичний вираз (наприклад, 23+12): ")

if '+' in exp:
    oper = exp.split('+')
    res = int(oper[0]) + int(oper[1])
elif '-' in exp:
    oper = exp.split('-')
    res = int(oper[0]) - int(oper[1])
elif '*' in exp:
    oper = exp.split('*')
    res = int(oper[0]) * int(oper[1])
elif '/' in exp:
    oper = exp.split('/')
    if int(oper[1]) == 0:
        print("Помилка: ділення на нуль неможливе.")
    else:
        res = int(oper[0]) / int(oper[1])
else:
    print("Недопустимий вираз. Використовуйте лише +, -, *, /.")


print("Результат:", res)



# У списку цілих, заповненому випадковими числами, визначити мінімальний і
# максимальний елементи, порахувати кількість від'ємних елементів, порахувати
# кількість додатних елементів, порахувати кількість нулів. Результати вивести на
# екран

import random

# Генеруємо список випадкових цілих чисел
n = int(input("Введіть кількість елементів у списку: "))
arr = [random.randint(-10, 10) for _ in range(n)]

# Визначаємо потрібні значення
mins = min(arr)
maxi = max(arr)
count_neg = 0
count_pos = 0
count = arr.count(0)

for x in arr:
    if x > 0:
        count_neg += 1

for x in arr:
    if x < 0:
        count_pos += 1

print(f"""
Масив: {arr})
Мінімальний елемент: {mins}
Максимальний елемент: {maxi}
Кількість від'ємних елементів: {count_neg}
Кількість додатних елементів: {count_pos}
Кількість нулів: {count})
""")